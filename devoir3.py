# Afficher les nombres de 1 à 10
for i in range(1, 11):
    print(i, end=" ")  # Affiche le nombre suivi d'un espace

    # Initialisation du compteur
nombre = 1

# Boucle while pour calculer et afficher les carrés
while nombre <= 5: # Condition
    carre = nombre ** 2
    print(f"Le carré de {nombre} est {carre}")

    # Incrementation du compteur
    nombre += 1

    

